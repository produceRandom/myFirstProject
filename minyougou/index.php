	//characteristic
	function fly(className){
		var left = className + " " + "dl:lt(" + 2 + ")"
		var right = className + " " + "dl:gt(" + 2 + ")"
		var bottom = className + " " + "dl:eq(" + 2 + ")"
		$(left).addClass("fly-from-left")
		$(right).addClass("fly-from-right")
		$(bottom).addClass("fly-from-bottom")
	}
	


	

	$(window).scroll(function(){
		for (i = 0 ; i < $(".animate-fly").length ; i++) {
			var obj = ".animate-fly:eq(" + i + ")"
			var distanceTop = $(obj).offset().top
			var winHeight = window.innerHeight
			distanceTop = distanceTop - winHeight
			if ($(window).scrollTop() > distanceTop) {
				fly(obj)
			} 
		}
	})
